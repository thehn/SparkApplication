package kr.gaion.ceh.web.model;

/**
 * to store range of date, values were passed from browser
 * 
 * @author gaion
 *
 */
public class DateRange {

	String from;
	String to;

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

}
